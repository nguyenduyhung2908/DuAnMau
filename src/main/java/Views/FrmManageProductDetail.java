    private void loadTable(){
        model.setNumRows(0);
        List<ManageProductDetail> list = this.productDetailService.findAll();
        if(!list.isEmpty())
           for(ManageProductDetail mpd : list){
               model.addRow(new Object[]{
                   mpd.getId(),
                   mpd.getManageProduct(),
                   mpd.getManageManufacturer(),
                   mpd.getManageColor(),
                   mpd.getManageCategory(),
                   mpd.getWarrantyYear(),
                   mpd.getDecription(),
                   mpd.getQuantity(),
                   mpd.getImportPrice(),
                   mpd.getPrice()
               });
           }
    }
    private ManageProductDetail validateProductDetail(){
        ManageProduct manageProduct = (ManageProduct) cbxProduct.getSelectedItem();
        ManageColor manageColor = (ManageColor) cbxColor.getSelectedItem();
        ManageManufacturer manageManufacturer = (ManageManufacturer) cbxManufacturer.getSelectedItem();
        ManageCategory manageCategory = (ManageCategory) cbxCategory.getSelectedItem();
        int warranty = validator.getInt(txtWarranty.getText());
        String decription = validator.getString(txtDecription.getText());
        int quantity = validator.getInt(txtQuantity.getText());
        BigDecimal importPrice = validator.getBigDecimal(txtImportPrice.getText());
        BigDecimal price = validator.getBigDecimal(txtPrice.getText());
        ManageProductDetail result = new ManageProductDetail(null, manageProduct, manageCategory, manageColor, manageManufacturer, warranty, decription, quantity, importPrice, price);
        return result;
    }
