    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        ManageProductDetail manageProductDetail = this.validateProductDetail();
        if(manageProductDetail != null){
            String idInserted = this.productDetailService.insert(manageProductDetail);
            if(idInserted != null) loadTable();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProductDetail.getSelectedRow();
        if(selectedRow != -1){
            ManageProductDetail manageProductDetail =  this.validateProductDetail();
            if(manageProductDetail != null){
                String id = (String) model.getValueAt(selectedRow, 0);
                manageProductDetail.setId(id);
                boolean updated = this.productDetailService.update(manageProductDetail);
                if(updated) loadTable();
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = tblProductDetail.getSelectedRows();
        if(selectedRows.length > 0){
            String[] ids = new String[selectedRows.length];
            for(int i = 0 ; i < selectedRows.length ; i++){
                ids[i] = (String) model.getValueAt(selectedRows[i], 0);
            }
            boolean deleted = this.productDetailService.delete(ids);
            if(deleted) loadTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblProductDetailMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductDetailMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblProductDetail.getSelectedRow();
        ManageProduct manageProduct = (ManageProduct) model.getValueAt(selectedRow, 1);
        ManageColor manageColor = (ManageColor) model.getValueAt(selectedRow, 3);
        ManageManufacturer manageManufacturer = (ManageManufacturer) model.getValueAt(selectedRow, 2);
        ManageCategory manageCategory = (ManageCategory) model.getValueAt(selectedRow, 4);
        String warranty =String.valueOf(model.getValueAt(selectedRow, 5));
        String decription = (String) model.getValueAt(selectedRow, 6);
        String quantity = model.getValueAt(selectedRow, 7).toString();
        String importPrice = (String) model.getValueAt(selectedRow, 8).toString();
        String price = (String) model.getValueAt(selectedRow, 9).toString();
        txtDecription.setText(decription);
        txtImportPrice.setText(importPrice);
        txtPrice.setText(price);
        txtQuantity.setText(quantity);
        txtWarranty.setText(warranty);
        cbxCategory.setSelectedItem(manageCategory);
        cbxColor.setSelectedItem(manageColor);
        cbxManufacturer.setSelectedItem(manageManufacturer);
        cbxProduct.setSelectedItem(manageProduct);
    }//GEN-LAST:event_tblProductDetailMouseClicked

