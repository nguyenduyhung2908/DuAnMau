    private void loadTable(){
        listPaging = employeeService.findAll();
        model.setRowCount(0);
        for(ManageEmployee e : listPaging){
            model.addRow(new Object[]{
                e.getCode(),
                e.getLastName()+ e.getMiddleName() + e.getFirstName(),
                e.getGender(),
                e.getdOB(),
                e.getAddress(),
                e.getPhone(),
                e.getPassword(),
                e.getManageShop(),
                e.getManageRole(),
                e.getStatus()
            });
        }
    }
    private void loadComboxLeader(){
        List<ManageEmployee> listLeader = new ArrayList<>();
        if(!listPaging.isEmpty()){
            for(ManageEmployee e : listPaging){
                String nameRole = e.getManageRole().getName();
                if(nameRole.equalsIgnoreCase("quan ly") || nameRole.equalsIgnoreCase("truong phong")){
                    listLeader.add(e);
                    
                }
            }
            cbxLeader.setModel(new DefaultComboBoxModel(listLeader.toArray()));
        }else{
            cbxLeader.removeAllItems();
        }
    }
