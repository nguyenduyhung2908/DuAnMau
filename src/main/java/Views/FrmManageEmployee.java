    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        ManageEmployee manageEmployee = this.validateEmployee();
        if(manageEmployee != null){
            String id = this.employeeService.insert(manageEmployee);
            if(id != null) {
                loadTable();
                loadComboxLeader();
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblEmployee.getSelectedRow();
        if(selectedRow != -1){
            ManageEmployee manageEmployee = this.validateEmployee();
            if(manageEmployee != null){
            boolean isUpdated = this.employeeService.update(manageEmployee);
            if(isUpdated){
                loadTable();
                loadComboxLeader();
            }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = tblEmployee.getSelectedRows();
        String[] code = new String[selectedRows.length];
        for(int i = 0 ; i < selectedRows.length ; i++){
            code[i] = (String) model.getValueAt(selectedRows[i], 0);
        }
        boolean isDeleted = this.employeeService.delete(code);
        if(isDeleted){
            loadTable();
            loadComboxLeader();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmployeeMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblEmployee.getSelectedRow();
        String code = (String) model.getValueAt(selectedRow, 0);
        ManageEmployee e = this.employeeService.findByCode(code);
        
        if(e != null){
            txtAddress.setText(e.getAddress());
            txtCode.setText(e.getCode());
            txtFirstName.setText(e.getFirstName());
            txtLastName.setText(e.getLastName());
            txtMiddleName.setText(e.getMiddleName());
            txtPassword.setText(e.getPassword());
            txtPhone.setText(e.getPhone());
            txtStatus.setText(e.getStatus() + "");
            cbxGender.setSelectedItem(e.getGender());
            cbxRole.setSelectedItem(e.getManageRole());
            cbxShop.setSelectedItem(e.getManageShop());
            jDateChooser1.setDate(e.getdOB());
            cbxLeader.setSelectedItem(e.getLeader() != null ? e.getLeader() : 0);
        }
    }//GEN-LAST:event_tblEmployeeMouseClicked
    private ManageEmployee validateEmployee(){
        String code = validator.getString(txtCode.getText());
        String firstName = validator.getString(txtFirstName.getText());
        String middleName = validator.getString(txtMiddleName.getText());
        String lastName = validator.getString(txtLastName.getText());
        String gender = (String) cbxGender.getSelectedItem();
        Date dOB = validator.getDate(jDateChooser1.getDate());
        String address = validator.getString(txtAddress.getText());
        String phone = validator.getString(txtPhone.getText());
        String password = validator.getString(txtPassword.getText());
        ManageShop shop =(ManageShop) cbxShop.getSelectedItem();
        ManageRole role = (ManageRole) cbxRole.getSelectedItem();
        ManageEmployee leader = (ManageEmployee) cbxLeader.getSelectedItem();
        int status = validator.getInt(txtStatus.getText()); 
        ManageEmployee manageEmployee = new ManageEmployee(code, firstName, lastName, middleName, gender, dOB, address, phone, password, shop, role,leader, status);
        return manageEmployee;
    }
