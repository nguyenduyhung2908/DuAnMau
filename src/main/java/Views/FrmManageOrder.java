    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        ManageOrder order = this.validateOrder();
        if(order != null){
            String inserted =this.orderSevice.insert(order);
            if(inserted != null) loadTable();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrderMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblOrder.getSelectedRow();
        String code = (String) model.getValueAt(selectedRow, 0);
        ManageOrder order = this.orderSevice.findByCode(code);
        txtCode.setText(code);
        txtAddress.setText(order.getAddress());
        txtPhone.setText(order.getPhone());
        txtStatus.setText(""+order.getStatus()+"");
        txtReceiver.setText(order.getReceiver());
        jdcCreatedDate.setDate(order.getCreatedDate());
        jdcPaidDate.setDate(order.getPaidDate());
        jdcReceivedDate.setDate(order.getReceivedDate());
        jdcShipDate.setDate(order.getShipDate());
        cbxCustomer.setSelectedItem(order.getCustomer());
        cbxEmployee.setSelectedItem(order.getEmployee());
    }//GEN-LAST:event_tblOrderMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrder.getSelectedRow();
        if(selectedRow != -1){
            ManageOrder order = this.validateOrder();
            if(order != null){
                boolean updated = this.orderSevice.update(order);
                if(updated) loadTable();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = tblOrder.getSelectedRows();
        if(selectedRows.length > 0){
            String[] codes = new String[selectedRows.length];
            for(int i = 0 ; i< selectedRows.length;i++){
                codes[i] = (String) model.getValueAt(selectedRows[i], 0);
            }
            boolean deleted = this.orderSevice.delete(codes);
            if(deleted) loadTable();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

