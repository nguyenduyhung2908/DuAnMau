    private void loadTable(){
        model.setNumRows(0);
        List<ManageOrder> listPaging = this.orderSevice.findAll();
        for(ManageOrder o : listPaging){
            model.addRow(new Object[]{
                o.getCode(),
                o.getCreatedDate(),
                o.getPaidDate(),
                o.getShipDate(),
                o.getReceivedDate(),
                o.getStatus(),
                o.getReceiver(),
                o.getAddress(),
                o.getPhone(),
                o.getEmployee(),
                o.getCustomer()
            });
            
        }
    }
    private ManageOrder validateOrder(){
        String code = validator.getString(txtCode.getText());
        Date createdDate = validator.getDate(jdcCreatedDate.getDate());
        Date paidDate = validator.getDate(jdcPaidDate.getDate());
        Date shipDate = validator.getDate(jdcShipDate.getDate());
        Date receivedDate = validator.getDate(jdcReceivedDate.getDate());
        int status = validator.getInt(txtStatus.getText());
        String receiver = validator.getString(txtReceiver.getText());
        String address = validator.getString(txtAddress.getText());
        String phone = validator.getString(txtPhone.getText());
        ManageEmployee creator = (ManageEmployee) cbxEmployee.getSelectedItem();
        ManageCustomer customer = (ManageCustomer) cbxCustomer.getSelectedItem();
        ManageOrder order = new ManageOrder(customer, creator, code, createdDate, paidDate, shipDate, receivedDate, status, receiver, address, phone);
        return order;
    }
