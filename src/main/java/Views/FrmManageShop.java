    private void loadTable(){
        listManageShop = this.manageShopService.findAll();
        model.setNumRows(0);
        for(ManageShop c : listManageShop){
            model.addRow(new Object[]{
                c.getCode(),
                c.getName(),
                c.getAddress(),
                c.getCity(),
                c.getCountry()
            });
        }
    }
