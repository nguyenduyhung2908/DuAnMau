    private void tblCuaHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCuaHangMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblCuaHang.getSelectedRow();
        String ma = (String) model.getValueAt(selectedRow, 0);
        String ten = (String) model.getValueAt(selectedRow, 1);
        String diaChi = (String) model.getValueAt(selectedRow, 2);
        String thanhPho = (String) model.getValueAt(selectedRow, 3);
        String quocGia = (String) model.getValueAt(selectedRow, 4);
        
        txtMa.setText(ma);
        txtTen.setText(ten);
        txtDiaChi.setText(diaChi);
        txtQuocGia.setText(quocGia);
        txtThanhPho.setText(thanhPho);
    }//GEN-LAST:event_tblCuaHangMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCuaHang.getSelectedRow();
        if( selectedRow != -1){
            ManageShop manageShop = this.validateCuaHang();
            if(this.manageShopService.update(manageShop))
                loadTable();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = tblCuaHang.getSelectedRows();
        String[] codes = new String[selectedRows.length];
        for(int i = 0; i < selectedRows.length; i++){
            codes[i] = (String) model.getValueAt(selectedRows[i], 0);
        }
            loadTable();
    }//GEN-LAST:event_btnXoaActionPerformed
    private ManageShop validateCuaHang(){
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String diaChi = txtDiaChi.getText();
        String thanhPho = txtThanhPho.getText();
        String quocGia = txtQuocGia.getText();
        return new ManageShop(ma, ten, diaChi, thanhPho, quocGia);
    }
