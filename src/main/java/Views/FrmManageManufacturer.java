    private void loadTable(){
        List<ManageManufacturer> listPaging = manufacturerService.findAll();
        model.setNumRows(0);
        if(!listPaging.isEmpty()){
            for(ManageManufacturer p : listPaging){
                model.addRow(new Object[]{
                    p.getCode(),
                    p.getName()
                });
            }
        }
    }

