    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        ManageManufacturer product = this.validateProduct();
        if(product != null){
            String idInserted = this.manufacturerService.insert(product);
            if(idInserted != null) loadTable();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManufacturer.getSelectedRow();
        if(selectedRow != -1){
            ManageManufacturer product = this.validateProduct();
            if(product != null){
                boolean isUpdated = this.manufacturerService.update(product);
                if(isUpdated) loadTable();
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int[] selectedRow = tblManufacturer.getSelectedRows();
        String[] codes = new String[selectedRow.length];
        for(int i = 0 ; i < codes.length; i++){
            codes[i] = (String) model.getValueAt(selectedRow[i], 0);
        }
        boolean isDeleted = this.manufacturerService.delete(codes);
        if(isDeleted) loadTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblManufacturerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblManufacturerMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblManufacturer.getSelectedRow();
        String code = (String) model.getValueAt(selectedRow, 0);
        String name = (String) model.getValueAt(selectedRow, 1);
        txtCode.setText(code);
        txtName.setText(name);
    }//GEN-LAST:event_tblManufacturerMouseClicked
    private ManageManufacturer validateProduct(){
        String code = validator.getString(txtCode.getText());
        String name = validator.getString(txtName.getText());
        return new ManageManufacturer(code, name);
    }   
